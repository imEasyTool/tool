int	ft_strcmp(char *s1, char *s2)
{
	int l;

	l = 0;
	while (s1[l] != '\0' && s2[l] != '\0' && s1[l] == s2[l])
		l++;
	if (s1[l + 1] == '\0' && s2[l + 1] == '\0')
		return (0);
	else
		return (s1[l] - s2[l]);
}
